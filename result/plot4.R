Sys.setlocale('LC_TIME','en_US.UTF-8')
df <- read.csv('subset.txt',sep=";")
x <- paste(df$Date,df$Time)
dt = strptime(x,"%d/%m/%Y %H:%M:%S",tz="PST")
png("plot4.png",width = 480, height = 480)
par(mfrow=c(2,2))
plot(dt,df$Global_active_power,type='l',ylab='Global Active Power(kilowatts)',xlab='')
plot(dt,df$Voltage,type='l',xlab='datetime',ylab='Voltage')
plot(dt,df$Sub_metering_1,type='l',ylab="Energy sub metering",xlab="")
lines(dt,df$Sub_metering_3,col='blue')
lines(dt,df$Sub_metering_2,col='red')
legend('topright',lty=1,col=c('black','red','blue'),legend=c('Sub_metering_1','Sub_metering_2','Sub_metering_3'))
plot(dt,df$Global_reactive_power,type='l',xlab='datetime',ylab='Global reactive power')
dev.off()
